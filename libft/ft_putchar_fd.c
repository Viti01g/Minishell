#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

//*****************************************************************************
// INPUT
// c: El carácter a enviar.
// fd: El file descriptor sobre el que escribir.
//*****************************************************************************
// OUTPUT
// Nada
//*****************************************************************************
// DESCRIPTION
// Envía el carácter ’c’ al file descriptor especificado.
//*****************************************************************************
